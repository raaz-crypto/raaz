        , testGroup "AES192 ECB Reference" $ standard192 r192E r192D
        , testGroup "AES256 ECB Reference" $ standard256 r256E r256D
        , testGroup "AES128 ECB CPortable" $ standard128 c128E c128D
        , testGroup "AES192 ECB CPortable" $ standard192 c192E c192D
        , testGroup "AES256 ECB CPortable" $ standard256 c256E c256D
