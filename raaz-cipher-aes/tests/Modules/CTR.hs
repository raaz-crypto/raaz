standard128 :: (Gadget g1, Gadget g2, Initializable (PrimitiveOf g1), Initializable (PrimitiveOf g2))
            => g1 -> g2 -> [Test]
standard128 g1 g2 = testStandardCiphers g1 standard128Vector "AES128 CTR Encryption" ++
                    testStandardCiphers g2 (toDecryption standard128Vector) "AES128 CTR Decryption"

standard192 :: (Gadget g1, Gadget g2, Initializable (PrimitiveOf g1), Initializable (PrimitiveOf g2))
            => g1 -> g2 -> [Test]
standard192 g1 g2 = testStandardCiphers g1 standard192Vector "AES192 CTR Encryption" ++
                    testStandardCiphers g2 (toDecryption standard192Vector) "AES192 CTR Decryption"

standard256 :: (Gadget g1, Gadget g2, Initializable (PrimitiveOf g1), Initializable (PrimitiveOf g2))
            => g1 -> g2 -> [Test]
standard256 g1 g2 = testStandardCiphers g1 standard256Vector "AES256 CTR Encryption" ++
                    testStandardCiphers g2 (toDecryption standard256Vector) "AES256 CTR Decryption"

cportableVsReference :: ( Gadget g1
                        , Gadget g2
                        , (PrimitiveOf g1) ~ (PrimitiveOf g2)
                        , Initializable (PrimitiveOf g2)
                        , Eq (PrimitiveOf g1))
                     => g1 -> g2 -> IV (PrimitiveOf g1) -> Test
cportableVsReference g1 g2 iv = testGadget g1 g2 iv "CPortable vs Reference"

